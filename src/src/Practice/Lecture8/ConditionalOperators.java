package Practice.Lecture8;

public class ConditionalOperators {
    public static void main(String[] args) {
        int a = 5;
        int b = 2;
        if (a > b) {
            System.out.println("A > B");
        } else if (a < b) {
            System.out.println("A < B");
        } else {
            System.out.println("A = B");
        }
        if (a > 0) {
            System.out.println("A > 0");
        }
    }
}
